import 'package:cloud_firestore/cloud_firestore.dart';
import '../../domain/entities/sermon_entity.dart';

class SermonModel extends SermonEntity {
  SermonModel({
    required super.id,
    required super.title,
    required super.preacherName,
    super.preacherId,
    required super.theme,
    required super.bibleText,
    required super.description,
    required super.congregationId,
    required super.audioUrl,
    super.audioPath,
    super.contentType,
    super.externalUrl,
    super.externalPlatform,
    super.articleContent,
    required super.durationInSeconds,
    super.transcription,
    super.summary,
    super.keyPoints,
    super.keyVerse,
    required super.processingStatus,
    super.processingError,
    required super.createdBy,
    required super.createdAt,
    required super.updatedAt,
    required super.sermonDate,
    required super.isPublished,
  });

  factory SermonModel.fromMap(Map<String, dynamic> map, String id) {
    return SermonModel(
      id: id,
      title: map['title'] ?? '',
      preacherName: map['preacherName'] ?? '',
      preacherId: map['preacherId'],
      theme: map['theme'] ?? '',
      bibleText: map['bibleText'] ?? '',
      description: map['description'] ?? '',
      congregationId: map['congregationId'] ?? '',
      audioUrl: map['audioUrl'] ?? '',
      audioPath: map['audioPath'],
      contentType: SermonContentType.values.firstWhere(
        (e) => e.name == map['contentType'],
        orElse: () => SermonContentType.uploadedAudio,
      ),
      externalUrl: map['externalUrl'],
      externalPlatform: map['externalPlatform'],
      articleContent: map['articleContent'],
      durationInSeconds: map['durationInSeconds'] ?? 0,
      transcription: map['transcription'],
      summary: map['summary'],
      keyPoints: List<String>.from(map['keyPoints'] ?? []),
      keyVerse: map['keyVerse'],
      processingStatus: ProcessingStatus.values.firstWhere(
        (e) => e.name == map['processingStatus'],
        orElse: () => ProcessingStatus.pending,
      ),
      processingError: map['processingError'],
      createdBy: map['createdBy'] ?? '',
      createdAt: (map['createdAt'] as Timestamp).toDate(),
      updatedAt: (map['updatedAt'] as Timestamp).toDate(),
      sermonDate: (map['sermonDate'] as Timestamp).toDate(),
      isPublished: map['isPublished'] ?? false,
    );
  }

  Map<String, dynamic> toMap() {
    return {
      'title': title,
      'preacherName': preacherName,
      'preacherId': preacherId,
      'theme': theme,
      'bibleText': bibleText,
      'description': description,
      'congregationId': congregationId,
      'audioUrl': audioUrl,
      'audioPath': audioPath,
      'contentType': contentType.name,
      'externalUrl': externalUrl,
      'externalPlatform': externalPlatform,
      'articleContent': articleContent,
      'durationInSeconds': durationInSeconds,
      'transcription': transcription,
      'summary': summary,
      'keyPoints': keyPoints,
      'keyVerse': keyVerse,
      'processingStatus': processingStatus.name,
      'processingError': processingError,
      'createdBy': createdBy,
      'createdAt': Timestamp.fromDate(createdAt),
      'updatedAt': Timestamp.fromDate(updatedAt),
      'sermonDate': Timestamp.fromDate(sermonDate),
      'isPublished': isPublished,
    };
  }

  factory SermonModel.fromEntity(SermonEntity entity) {
    return SermonModel(
      id: entity.id,
      title: entity.title,
      preacherName: entity.preacherName,
      preacherId: entity.preacherId,
      theme: entity.theme,
      bibleText: entity.bibleText,
      description: entity.description,
      congregationId: entity.congregationId,
      audioUrl: entity.audioUrl,
      audioPath: entity.audioPath,
      contentType: entity.contentType,
      externalUrl: entity.externalUrl,
      externalPlatform: entity.externalPlatform,
      articleContent: entity.articleContent,
      durationInSeconds: entity.durationInSeconds,
      transcription: entity.transcription,
      summary: entity.summary,
      keyPoints: entity.keyPoints,
      keyVerse: entity.keyVerse,
      processingStatus: entity.processingStatus,
      processingError: entity.processingError,
      createdBy: entity.createdBy,
      createdAt: entity.createdAt,
      updatedAt: entity.updatedAt,
      sermonDate: entity.sermonDate,
      isPublished: entity.isPublished,
    );
  }
}
